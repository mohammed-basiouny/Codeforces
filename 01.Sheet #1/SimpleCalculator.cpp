/*Given two numbers X and Y. Print the summation and multiplication and subtraction of these 2 numbers.*/


#include <iostream>
using namespace std;
int main() {

    long long num1,num2;
    cin >> num1>>num2;
    cout <<num1<<" + "<<num2<<" = "<<num1+num2<<endl;
    cout <<num1<<" * "<<num2<<" = "<<num1*num2<<endl;
    cout <<num1<<" - "<<num2<<" = "<<num1-num2<<endl;
}
